const historyContainer = document.getElementById("history-posts");
const modernContainer = document.getElementById("modern-posts");
const dialog = document.getElementById("post-dialog");
const dialogTitle = document.getElementById("dialog-title");
const dialogMeta = document.getElementById("dialog-meta");
const dialogBody = document.getElementById("dialog-body");
const closeButton = document.getElementById("close-dialog");

function renderPosts(list, container) {
  list.forEach((post, index) => {
    const card = document.createElement("article");
    card.className = "post-card";

    const title = document.createElement("h3");
    title.textContent = post.title;

    const summary = document.createElement("p");
    summary.textContent = post.summary;

    const button = document.createElement("button");
    button.type = "button";
    button.textContent = "Читать статью";
    button.dataset.index = String(index);

    button.addEventListener("click", () => openArticle(post));

    card.append(title, summary, button);
    container.append(card);
  });
}

function openArticle(post) {
  dialogTitle.textContent = post.title;
  dialogMeta.textContent = post.date;
  dialogBody.innerHTML = "";

  post.article.forEach((paragraph) => {
    const p = document.createElement("p");
    p.textContent = paragraph;
    dialogBody.append(p);
  });

  dialog.showModal();
}

closeButton.addEventListener("click", () => dialog.close());
dialog.addEventListener("click", (event) => {
  if (event.target === dialog) {
    dialog.close();
  }
});

renderPosts(posts.history, historyContainer);
renderPosts(posts.modern, modernContainer);
